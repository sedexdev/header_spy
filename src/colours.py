"""
Terminal colours class module
"""

# pylint: disable=too-few-public-methods


class TerminalColours:
    """
    Colours for displaying success or failure of
    request on stdout
    """
    GREEN = '\033[92m'
    RED = '\033[91m'
    PURPLE = '\033[95m'
    YELLOW = '\033[33m'
    BLUE = '\033[96m'
